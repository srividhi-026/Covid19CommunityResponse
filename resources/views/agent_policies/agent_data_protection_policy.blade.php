        <h5>The Data Protection Principles Explained</h5>

        <p>
            This Policy aims to ensure compliance with the data protection and privacy law, particularly the GDPR.
            The GDPR sets out the following principles with which any party handling Personal Data must comply.
            Article 5 in the GDPR states that all Personal Data must be:
        </p>

        <ul>
            <li>
                &nbsp; - Processed lawfully, fairly and in a transparent manner in relation to the data subject;
            </li>
            <li>
                &nbsp; - Collected for specified, explicit and legitimate purposes and not further processed in
                a manner that is incompatible with those purposes; further processing for archiving purposes in the
                public interest, scientific or historical research purposes or statistical purposes shall not be considered
                to be incompatible with the initial purposes subject to appropriate safeguards, and provided that
                there is no risk of breaching the privacy of the data subject.
            </li>
            <li>
                &nbsp; - Adequate, relevant and limited to what is necessary in relation to the purposes for which
                it is processed;
            </li>
            <li>
                &nbsp; - Accurate and where necessary, kept up to date; every reasonable step must be taken
                to ensure that Personal Data that is inaccurate, having regard to the purposes for
                which they are processed is erased or rectified without delay;
            </li>
            <li>
                &nbsp; - Kept in a form which permits identification of data subjects for no longer than is
                necessary for the purposes for which the Personal Data is processed; Personal Data
                may be stored for longer periods insofar as the Personal Data will be processed solely
                for archiving purposes in the public interest, scientific or historical research purposes
                or statistical purposes subject to implementation of the appropriate technical and
                organisational measures required by the GDPR in order to safeguard the rights and
                freedoms of the data subject;
            </li>
            <li>
                &nbsp; - Processed in a manner that ensures appropriate security of the Personal Data, including
                protection against unauthorised or unlawful processing and against accidental loss, destruction
                or damage, using appropriate technical or organisational measures;
            </li>
            <li>
                &nbsp; - Article 5(2) states that the Controller is responsible for and must be able to demonstrate
                compliance with the Data Protection Principles.
            </li>
        </ul>

        <h6><i>Lawful, Fair and Transparent Data Processing</i></h6>

        <p>
            The GDPR seeks to ensure that Personal Data is processed lawfully, fairly and transparently, without
            adversely affecting the rights of the data subject. The GDPR states that processing of Personal Data
            shall be lawful if at least one of the following applies:
        </p>

        <ul>
            <li>
                &nbsp; -  Processing is necessary for the purposes of the legitimate interests pursued by the
                controller or by a third party, except where such interests are overridden by the fundamental rights
                and freedoms of the data subject which require protection of Personal Data, in particular where the
                data subject is a child;
            </li>
            <li>
                &nbsp; - Processing is necessary for the performance of a contract to which the data subject
                is a party or in order to take steps at the request of the data subject prior to entering
                into a contract;
            </li>
            <li>
                &nbsp; - Processing is necessary for compliance with a legal obligation to which the controller
                is subject;
            </li>
            <li>
                &nbsp; - The data subject has given consent to the processing of his or her Personal Data for
                one or more specific purposes;
            </li>
            <li>
                &nbsp; - Processing is necessary in order to protect the vital interests of the data subject or
                of another natural person;
            </li>
            <li>
                &nbsp; - Processing is necessary for the performance of a task carried out in the public interest
                or in the exercise of official authority vested in the controller.
            </li>
            <li>
                    &nbsp; -
            </li>
        </ul>

        <p>
            The Organisation will ensure that at least one of the conditions outlined above will be satisfied whenever
            any processing activities take place.
        </p>
        <p>
            In order to obtain Personal Data fairly and in a transparent manner, the Organisation will make the data
            subject aware of the following at the time the data is collected directly:
        </p>

        <ul>
            <li>
                &nbsp; - Identity of the Data Controller and the DPCO
            </li>
            <li>
                &nbsp; - Purpose and legal basis for processing. An explanation of the legitimate interest of the
                Organisation will be provided if it is being used as the legal basis.
            </li>
            <li>
                &nbsp; - Data subject’s rights to withdraw consent, request access, rectification or restriction
                of processing.
            </li>
            <li>
                &nbsp; - Data subject’s rights to complain to the relevant supervisory authority
            </li>
            <li>
                &nbsp; - Recipients of the Personal Data.
            </li>
            <li>
                &nbsp; - Storage periods or criteria used to determine the length of storage.
            </li>
            <li>
                &nbsp; - Legal basis for intended international transfer of data to a third country or organisation,
                including the fact that either the receiving country has an adequacy decision from the relevant
                supervisory authority or other appropriate safeguards are in place and how to obtain a copy.
            </li>
        </ul>

        <p>
            In situations where the data is not being collected directly from the data subject, the Organisation
            will provide the source along with the other information listed above to the data subject within a
            reasonable period after obtaining the data but not more than one month. Information will not be provided
            to the data subject if it will require disproportionate effort or it would render it impossible or
            seriously impair the purpose of the data processing.
        </p>

        <p>
            The Data Subjects’ Personal Data will not be disclosed to a third party other than to a party
            contracted to the Organisation and operating on its behalf.
        </p>

        <h6><i>Processed for Specified, Explicit and Legitimate Purposes</i></h6>

        <p>
