        <h5>Special Category Data</h5>

        <p>
            At times the Organisation may be required to process special category data. The Data Subject will be
            notified of this at the data collection point. The Organisation will only process special category data
            on one of the following grounds:
        </p>

        <ul>
            <li>
                &nbsp; - Explicit Consent – The individual has given their clear and unambiguous explicit consent.
            </li>
            <li>
                &nbsp; - Legal obligation related to employment – The processing is necessary for the purposes of
                carrying out a legal obligation and exercising specific rights of the organisation or of the
                individual in the field of employment, social security law or for a collective agreement.
            </li>
            <li>
                &nbsp; - Vital interests – The processing is necessary to protect the vital interests of the
                individual or of another person where the data subject is physically or legally incapable
                of giving consent.
            </li>
            <li>
                &nbsp; - Not-for-Profit bodies – The processing is carried out in the course of the legitimate
                activities, with appropriate safeguards by the Not-for-Profit body and on condition that the
                processing only relates to members or related persons, or to former members of the body, or to
                persons who have regular contact with it in connection with its purposes and the Personal Data
                is not disclosed outside that body without consent.
            </li>
            <li>
                &nbsp; - Public Information – the processing relates to Personal Data which is manifestly made
                public by the individual.
            </li>
            <li>
                &nbsp; - Legal Claims – The processing is necessary for the establishment, exercise or defence
                of legal claims or whenever courts are acting in their judicial capacity.
            </li>
            <li>
                &nbsp; - Substantial public interest - The processing is necessary for reasons of substantial
                public interest.
            </li>
            <li>
                &nbsp; - Healthcare – The processing is necessary for the purposes of preventive or occupational
                medicine,(i.e., healthcare purposes), for the assessment of the working capacity of the employee,
                medical diagnosis, the provision of health or social care or treatment or the management of health
                or social care systems and services on the basis of EU or Irish law, or pursuant to contract with
                a health professional and is subject to suitable safeguards.
            </li>
            <li>
                &nbsp; - Public Heath - The processing is necessary for reasons of public interest purposes and
                is subject to suitable safeguards.
            </li>
            <li>
                &nbsp; - Archiving – The processing is necessary for archiving scientific or historical research
                purposes or statistical purposes and based on EU or Irish law.
            </li>
