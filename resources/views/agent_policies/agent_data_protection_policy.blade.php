        <h5>When is a data protection notice required?</h5>

        <ul>
            <li>
                &nbsp; - Where information is being collected directly from an individual, a Data Protection Notice
                must be provided at the point at which the data is collected.
            </li>
            <li>
                &nbsp; - Where information is obtained from another source, a Data Protection Notice must be provided:
            </li>
            <li>
                &nbsp;&nbsp; - at least one month after obtaining the data;
            </li>
            <li>
                &nbsp;&nbsp; - if Personal Data is to be used to communicate with the data subject at the latest
                at the time of the first communication with the data subjects;
            </li>
            <li>
                &nbsp; - If disclosure to another recipient is envisaged, at the latest when Personal Data are
                first disclosed.
            </li>
        </ul>

        <h5>What needs to be included in a data protection notice?</h5>

        <h6>
            <b>
                Data protection notices must contain specific information (set out in the legislation) which
                informs data subjects of:
            </b>
        </h6>

        <ul>
            <li>
                &nbsp; - who is collecting the data
            </li>
            <li>
                &nbsp; - why it is being collected
            </li>
            <li>
                &nbsp; - what legal basis is being relied upon to process the data
            </li>
            <li>
                &nbsp; - how it will be processed
            </li>
            <li>
                &nbsp; - how long it will be kept for
            </li>
            <li>
                &nbsp; - who it will be disclosed to
            </li>
            <li>
                &nbsp; - what rights people have in relation to their own data
            </li>
        </ul>

        <h5>Individuals must also be made aware of:</h5>

        <ul>
            <li>
                &nbsp; - the right to lodge a complaint with the Data Protection Commission,
            </li>
            <li>
                &nbsp; - the consequences of failure by the Data Controller to provide the data, and
            </li>
            <li>
                &nbsp; - the existence of automated decision making, including profiling.
            </li>
        </ul>

