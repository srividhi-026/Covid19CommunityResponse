        </p>

        <ul>
            <li>
                &nbsp; - the consent of the data subject has been obtained, or
            </li>
            <li>
                &nbsp; - if the further processing is for archiving purposes in the public interest or scientific
                and historical research or statistical purposes and the appropriate safeguards are in
                place and there is no risk of breaching the privacy of the data subject.
            </li>
        </ul>

        <h6></i>Adequate, Relevant and Limited Data Processing</i></h6>

        <p>
            The Organisation follows this data minimisation principle and only collect and process
            Personal Data for and to the extent necessary for the specific purpose(s) informed to data
            subjects.
        </p>

        <h6></i>Accuracy of Data and Keeping Data Up to Date</i></h6>

        <p>
            The Organisation will ensure that all Personal Data collected and processed is kept accurate
            and up-to-date. The accuracy of data will be checked when it is collected. Where any inaccurate or
            out-of-date data is found, all reasonable steps will be taken without delay to amend or erase that
            data, as appropriate.
        </p>

        <ul>
            <li>
                Amend inaccurate data which has been notified to the Organisation by the Data
                Subject or is revealed as a result of a subject access request.
            </li>
        </ul>

        <h6><i>Timely Processing</i></h6>

        <p>
            The Organisation follows this storage limitation principle and does not keep Personal Data
            for any longer than is necessary in light of the purposes for which that data was originally
            collected and processed.
        </p>

        <p>
            The Organisation will verify whether statutory data retention periods exist in relation to the
            type of processing e.g., Personal Data may need to be kept in order to comply with tax, health and safety,
            or employment regulations etc. If the law is silent, internal data retention periods will be set to meet the
            storage limitation principle.
        </p>
        <p>
            Retention periods will be set considering the purpose or purpose for which the data is collected and
            used, and once the storage periods expire, data will be securely deleted/destroyed in the absence of a
            sound new lawful basis to retain it. However, Personal Data may be stored for longer periods by the
            Organisation insofar as the Personal Data will be processed solely for archiving purposes in the public
            interest, scientific, historical research or statistical purposes ensuring appropriate safeguards are
            in place i.e.irreversibly anonymised.
        </p>

        <p>The Organisation keeps record of this in the “Record of Processing Activities” document.</p>

        <h6><i>Secure Processing</i></h6>

        <p>
            The Organisation will ensure that all Personal Data collected and processed is kept secure
            and protected against unauthorised or unlawful processing and against accidental loss,
            destruction or damage. The state of technological development, the cost of implementing
            the measures, the nature of the data concerned and the degree of harm that might result
            from unauthorised or unlawful processing are all considered when the Organisation are
            determining the security measures that are put in place
        </p>

        <h6><i>Accountability</i></h6>

        <p>
            Under the GDPR, organisations are obliged to demonstrate that their processing activities
            are compliant with the Data Protection Principles. The principle of accountability seeks to
            guarantee the enforcement of the Principles.
        </p>

