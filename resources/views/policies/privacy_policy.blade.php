    <h5>What are the data protection principles and rules?</h5>

    <p>We aim to comply with the following principles found in Data Protection Law:</p>

    <ul>
        <li>
            &nbsp; - Lawfulness, fairness and transparency – Personal data must be processed lawfully, fairly and in
            a transparent manner.
        </li>
        <li>
            &nbsp; - Purpose Limitation. Personal data must be collected for specified, explicit and legitimate
            purposes and not further processed in a manner that is incompatible with those purposes.
        </li>
        <li>
            &nbsp; - Data minimisation – Personal Data must be adequate, relevant and limited to what is necessary
            in relation to purposes for which they are processed.
        </li>
        <li>
            &nbsp; - Accuracy – Personal data must be accurate and, where necessary, kept up to date. Inaccurate
            Personal Data should be corrected or deleted.
        </li>
        <li>
            &nbsp; - Retention – Personal data should be kept in an identifiable format for no longer than is
            necessary.
        </li>
        <li>
            &nbsp; - Integrity and confidentiality – Personal data should be kept secure.
        </li>
        <li>
            &nbsp; - Accountability – Under the GDPR, we must not only comply with the above six general principles
            but we must be able to demonstrate that we comply by documenting and keeping records of all decisions.
        </li>
    </ul>

